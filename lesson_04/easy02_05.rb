# this is local variable.  this type of variable
# users snake case as a convention
excited_dog = "excited dog"

# this is an instance variable.  it holds the value of an attribute
# of an instance of a class.  It begins with @
@excited_dog = "excited_dog"

# this is a class variable.  it holds the value of an attribute
# of the class.  begins with @@.
@@excited_dog = "excited_dog"
